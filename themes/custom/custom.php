	public function parse_settings($settings){
		$option = function($option) use ($settings){
			$theme_prefix = $this->theme_prefix.'__';
			return $settings[$theme_prefix.$option];
		};
		
		
		// Parse Settings
		// ==========================================================================
		$_c = new Colors();
		
		
		// Theme
		$theme = $option('scheme');

		/**
		 * Theme by userrole
		 * @version 1.0.0
		 * @since 1.0.0
		 */
		$schemes_by_role 		= $theme;
		$current_userrole 		= Plus_admin::helper()->get_current_user_roles()[0];
		$current_scheme 		= (isset($schemes_by_role['userrole']['cssf__'.$current_userrole])) ? $schemes_by_role['userrole']['cssf__'.$current_userrole] : $schemes_by_role['userrole']['cssf_default_scheme'];
		$current_scheme_id 		= $current_scheme['scheme_id'];
		$current_scheme_scheme 	= $current_scheme['scheme'];
		if ($current_scheme_id == 'default_theme' && $current_scheme_scheme == 'default_theme') {
			$current_scheme 		= $schemes_by_role['userrole']['cssf_default_scheme'];
			$current_scheme_id 		= $current_scheme['scheme_id'];
			$current_scheme_scheme 	= $current_scheme['scheme'];
		}
		$theme = json_decode($current_scheme_scheme,true);

		/*  Variables for dynamic button colors
    		---------------------------------------------------------------------- */
		$button_base_normal_text 			= $theme['button-base-normal-text'];
		$button_base_normal_background 		= $theme['button-base-normal-background'];
		$button_base_normal_border_color 	= $theme['button-base-normal-border-color'];

		$button_primary_normal_text 		= $theme['button-primary-normal-text'];
		$button_primary_normal_background 	= $theme['button-primary-normal-background'];
		$button_primary_normal_border_color = $theme['button-primary-normal-border-color'];

		$button_success_normal_text 		= $theme['button-success-normal-text'];
