(function( $, window, undefined ) {
    'use strict';

    var $document 	= $(document),
        $window 	= $(window),
        $body 		= $(document.body);

    // window.PLUS_SETTINGS = {};
    // var PLUS_SETTINGS = window.PLUS_SETTINGS;
    
	window._PLUS_ADMIN = {};
	var _PLUS_ADMIN = window._PLUS_ADMIN;

	_PLUS_ADMIN.settings = {};
	var settings = _PLUS_ADMIN.settings;


	// Remove Core elements
    _PLUS_ADMIN.removeElements = function(){
        $('#login form p.forgetmenot').remove();
        $('#login p#nav').remove();
        $('#login #backtoblog').remove();
    };
	
	
	// Custom "remember me" checkbox style
    _PLUS_ADMIN.checkbox = function(){
        $('.cs-plus-admin-forgetmenot input#rememberme').iCheck({
            handle: 'checkbox',
            checkboxClass: 'cs_plus_checkbox',
        });
    }

	// Input fields animation
    _PLUS_ADMIN.checkInput = function($input){
        var value = $input.val();
        if (value !== ''){
            $input.parents('label').addClass('cs-autofilled');
        }
        $input.on('animationstart',function(target){
            var animationName = target.originalEvent.animationName;
            if (animationName == 'csAutoFillStart'){
                $(this).parent().addClass('cs-autofilled');
            }
            if (animationName == 'csAutoFillCancel'){
                // $(this).parent().removeClass('cs-autofilled');
            }
        }).on('focus',function(){
            $(this).parent().addClass('cs-autofilled').addClass('cs-focused');
        }).on('blur',function(){
            if ($(this).val().length == 0){
                $(this).parent().removeClass('cs-autofilled');
            }
            $(this).parent().removeClass('cs-focused');
        });
    }

	// Add custom settings
    _PLUS_ADMIN.addElements = function(){
        if (settings.use_placeholder) {
            $('#login input#user_login').attr('placeholder','Username');
            $('#login input#user_pass').attr('placeholder','Password');
            $('#login input#user_email').attr('placeholder','E-mail');
        }

        var $user_login = $('#login input#user_login'),
            $user_pass  = $('#login input#user_pass'),
            $user_email = $('#login input#user_email');
        
        this.checkInput($user_login);
        this.checkInput($user_pass);
        this.checkInput($user_email);
	};

	// Window Size listener
	_PLUS_ADMIN.windowSize = function(){
		var $wrapper 	= $('#cs-plus-admin-login-page-wrapper'),
			is_mobile 	= jQuery.browser.mobile,
			width 		= $(window).width(),
			height 		= $(window).height(),
			orientation = window.orientation;

		var prefix 		= "plus_",
			wrapper_class 	= '';

		// Is Mobile
		$wrapper.data(prefix+'is-mobile',is_mobile);
		wrapper_class += (is_mobile) ? 'is_mobile' : 'is_desktop';
		
		// Orientation
		orientation = (orientation != 0) ? 'landscape' : 'portrait';
		$wrapper.data(prefix+'orientation',orientation);
		wrapper_class += ' '+orientation;

		// Size
		var size;  
		if (width <= 360){
			size = 'xxs';
		} else if (width <= 576){
			size = 'xs';
		} else if (width <= 768){
			size = 'sm';
		} else if (width <= 992){
			size = 'md';
		} else if (width <= 1200){
			size = 'xl';
		} else if (width > 1200){
			size = 'xxl';
		}
		$wrapper.data(prefix+'width',width).data(prefix+'height',height).data(prefix+'size',size);
		wrapper_class += ' size-'+size;

		$wrapper.removeClass('size-xxs size-xs size-sm size-md size-xl size-xxl is_mobile is_desktop landscape portrait').addClass(wrapper_class);

		// console.log("> Mobile:",is_mobile," > Size: ",width,"x",height," > Orientation: ",orientation);
	}


    $(document).ready(function() {
        // _PLUS_ADMIN.removeElements();
        _PLUS_ADMIN.checkbox();
		_PLUS_ADMIN.addElements();
		_PLUS_ADMIN.windowSize();
	});
	$(window).on('resize',function(){
		_PLUS_ADMIN.windowSize();
	});
})( jQuery, window );